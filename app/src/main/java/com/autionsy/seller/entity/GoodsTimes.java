package com.autionsy.seller.entity;

public class GoodsTimes {

    private String goodsCode;

    private String lookAtTimes;

    private String tradingVolume;

    public String getGoodsCode() {
        return goodsCode;
    }

    public void setGoodsCode(String goodsCode) {
        this.goodsCode = goodsCode;
    }

    public String getLookAtTimes() {
        return lookAtTimes;
    }

    public void setLookAtTimes(String lookAtTimes) {
        this.lookAtTimes = lookAtTimes;
    }

    public String getTradingVolume() {
        return tradingVolume;
    }

    public void setTradingVolume(String tradingVolume) {
        this.tradingVolume = tradingVolume;
    }
}
